    import sys

    t = TestBoundedRestartParity()
    t.test_both_files_use_the_same_restart_cap_and_window()
    t.test_both_files_have_a_rolling_window_filter_before_the_cap_check()
    t.test_both_files_stop_instead_of_looping_forever_once_capped()
    t2 = TestScheduledTaskSettingsParity()
    t2.test_both_use_atlogon_trigger()
    t2.test_both_use_the_same_settings_set_flags()
    t2.test_both_register_with_limited_run_level()
    t3 = TestNoClosableWindow()
    t3.test_no_closable_windowstyle_anywhere()
    t3.test_install_ps1_fallback_start_uses_hidden()
    print("all tests passed")
    sys.exit(0)
